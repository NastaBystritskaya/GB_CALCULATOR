package au.bystritskaia.calculator;

public class Calculator {

    public int sum(int a, int b) {
        return a + b;
    }

    public int dif(int a, int b) {
        return a - b;
    }

    public int mul (int a, int b) {
        return a * b;
    }

    public float div(int a, int b) {
        return (float) a / b;
    }


}
